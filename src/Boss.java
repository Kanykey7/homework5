public class Boss {
    private int helth;
    private int damage;
    private String defence;

    public void setHelth(int helth) {
        this.helth = helth;
    }

    public void setDamage(int damage) {
        this.damage = damage;
    }

    public void setDefence(String defence) {
        this.defence = defence;
    }

    public int getHelth() {
        return helth;
    }

    public int getDamage() {
        return damage;
    }

    public String getDefence() {
        return defence;
    }
}
